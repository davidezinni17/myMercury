package model;

public class Azienda {

    private String nomeAzienda;
    private String luogoAzienda;
    private String numeroAzienda;
    private String p_iva;



    public String getNomeAzienda() {
        return nomeAzienda;
    }

    public void setNomeAzienda(String nomeAzienda) {
        this.nomeAzienda = nomeAzienda;
    }

    public String getLuogoAzienda() {
        return luogoAzienda;
    }

    public void setLuogoAzienda(String luogoAzienda) {
        this.luogoAzienda = luogoAzienda;
    }

    public String getNumeroAzienda() {
        return numeroAzienda;
    }

    public void setNumeroAzienda(String numeroAzienda) {
        this.numeroAzienda = numeroAzienda;
    }

    public String getP_iva() {
        return p_iva;
    }

    public void setP_iva(String p_iva) {
        this.p_iva = p_iva;
    }
}
